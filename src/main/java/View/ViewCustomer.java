    //Start the action: INSERT a new customer on database
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        //Send data to controller about the new customer
        try {
            ControlCustomer objCtrCustomer = new ControlCustomer();
            Store objStore = new Store();

            String id = this.txtId.getText();
            String nombre = this.txtName.getText();
            String email = this.txtEmail.getText();
            String phone = this.txtPhone.getText();

            if (this.cbxStore.getSelectedItem().equals("Chicago")) {
                objStore.setCode(1);
                objStore.setNameStore("Chicago");
            } else if (this.cbxStore.getSelectedItem().equals("Boston")) {
                objStore.setCode(2);
                objStore.setNameStore("Boston");
            } else if (this.cbxStore.getSelectedItem().equals("Seattle")) {
                objStore.setCode(3);
                objStore.setNameStore("Seattle");
            } else if (this.cbxStore.getSelectedItem().equals("Miami")) {
                objStore.setCode(4);
                objStore.setNameStore("Miami");
            }

            objCtrCustomer.create(id, nombre, email, phone, objStore);
            JOptionPane.showMessageDialog(null, "Insertado correctamente");

            this.txtId.setText("");
            this.txtName.setText("");
            this.txtEmail.setText("");
            this.txtPhone.setText("");
            
            this.list();
            
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Error: " + error);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed
    
