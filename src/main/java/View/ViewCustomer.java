    //Start the action: CHECK for a customer from database
    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        
        try {
            ControlCustomer objCtrCustomer = new ControlCustomer();
            DefaultTableModel table = new DefaultTableModel();
            ArrayList<Customer> checkCustomer = new ArrayList<>();
            
            String name = this.txtNameCustomer.getText();
            checkCustomer = objCtrCustomer.check(name);

            table.addColumn("ID");
            table.addColumn("Name");
            table.addColumn("Email");
            table.addColumn("Phone");

            this.tableCustomer.setModel(table);

            String[] data = new String[4];

            for (int i = 0; i < checkCustomer.size(); i++) {
                data[0] = String.valueOf(checkCustomer.get(i).getId());
                data[1] = checkCustomer.get(i).getName();
                data[2] = checkCustomer.get(i).getEmail();;
                data[3] = String.valueOf(checkCustomer.get(i).getPhone());

                table.addRow(data);
            }

        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Error: " + error);
        }
        
    }//GEN-LAST:event_btnCheckActionPerformed
    
