    //Start the action: UPDATE a customer from database
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        try{
            ControlCustomer objCtrCustomer = new ControlCustomer();
            
            String id = this.txtIdCheck.getText();
            String name = this.txtNameCheck.getText();
            String email = this.txtEmailCheck.getText();
            String phone = this.txtPhoneCheck.getText();
            
            objCtrCustomer.update(id, name, email, phone);
            JOptionPane.showMessageDialog(null, "Actualizado correctamente");

            this.txtIdCheck.setText("");
            this.txtNameCheck.setText("");
            this.txtEmailCheck.setText("");
            this.txtPhoneCheck.setText("");
            
            this.list();
            
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "Error: " + error);
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed
    
